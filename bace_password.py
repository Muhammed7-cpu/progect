def base_password(password: str) -> int:
    """
    Проверка, есть ли пароль в базе популярных паролей.

    Аргументы:
        password (str): Пароль пользователя для проверки.

    Возвращает:
        int:
            2 — если пароль найден в базе популярных паролей (не надежный)
            1 — если пароль не найден в базе (надежный)
    """
    # Читаем файл со списком популярных паролей
    with open("common_passwords.txt", "r", encoding="utf-8") as file:
        common = file.read().splitlines()  # Список популярных паролей

    a = 1  # По умолчанию считаем пароль надежным

    # Если пароль есть в базе, считаем его слабым
    if password in common:
        a = 2

    return a
